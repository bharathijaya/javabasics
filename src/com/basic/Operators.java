package com.basic;

public class Operators {
	public static void main(String[]args)
	{
		int choice = 100;
		switch(choice)
		{
		case 10:
			System.out.println(10);
			break;
		case 30:
			System.out.println(30);
			break;
		case 100:
			System.out.println(100);
			break;
			default:
				System.out.println("default");
				break;
		}
			
	}
  
}
